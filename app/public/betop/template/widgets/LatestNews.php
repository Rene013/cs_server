	Even giants have to start somewhere. Two blobs spotted in the distant, ancient universe may be the seeds of the supermassive black holes that now dominate the core of every galaxy.

	The find could help solve a long-standing puzzle. We have seen black holes millions and even billions of times the mass of the sun far enough away that we think these behemoths existed back when the universe was less than a billion years old. But they shouldn’t have had the time to grow so large.

	Either these early black holes formed from massive stars and fattened up at breakneck speed by swallowing gas, or they  had a head start – by being born more than 100,000 times heavier than the sun.

	Now a team led by Fabio Pacucci of Italy’s Scuola Normale Superiore in Pisa thinks it has found two examples of such swollen baby black holes.
